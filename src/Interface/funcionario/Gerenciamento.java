    private void tblApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblApagarActionPerformed
        if(tblContatos.getSelectedRowCount () == 1){
        int row = tblContatos.getSelectedRow();
        int id = Integer.parseInt(tblContatos.getValueAt(row, 0).toString());
        int resposta = JOptionPane.showConfirmDialog(null, "Desejar deletar esse usu√°rio?");
        if (resposta == JOptionPane.YES_OPTION){
           ContatoDAO contatoDao =  new ContatoDAO();
           contatoDao.deletarUsuarioPorId(id);
           atualizarTabela();
       }
        }
        
        else {
        JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
    }//GEN-LAST:event_tblApagarActionPerformed

