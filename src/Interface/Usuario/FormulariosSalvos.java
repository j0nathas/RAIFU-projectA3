    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
       ConexaoFormulario dao = new ConexaoFormulario();
    DefaultTableModel model = (DefaultTableModel) tabelaFormulariosSalvos.getModel();
    model.setRowCount(0); // limpa a tabela

    for (ContatoFormulario formulario : dao.listarFormulariosSalvosPorUsuario(idUsuarioLogado)) {
        model.addRow(new Object[]{
            formulario.getId(),
            formulario.getData(),
            formulario.getNome(),
            formulario.getIdade(),
            formulario.getAreaInteresse(),
            formulario.getDescricao(),
            formulario.getStatus()
        });
    }
    }//GEN-LAST:event_formWindowOpened

