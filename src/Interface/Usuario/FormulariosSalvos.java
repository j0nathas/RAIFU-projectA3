    private void visualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_visualizarActionPerformed
        
        if(tabelaFormulariosSalvos.getSelectedRowCount () == 1){
            int row = tabelaFormulariosSalvos.getSelectedRow();
            int id = Integer.parseInt(tabelaFormulariosSalvos.getValueAt(row, 0).toString());
            new VisualizarFormulario(id).setVisible(true);
        }

        else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }
    }//GEN-LAST:event_visualizarActionPerformed

